#include "libft.h"

int		ft_wrdcount(char const *str, char c)
{
	int		res;
	int		i;

	res = 0;
	i = 0;
	while (str[i])
	{
		while (str[i] && str[i] == c)
			i++;
		if (str[i])
			res++;
		while (str[i] && str[i] != c)
			i++;
	}
	return (res);
}
