#include "libft.h"

int		ft_intlen(int c)
{
	int		i;
	long	nb;

	nb = (long)c;
	i = 0;
	if (nb < 0)
	{
		i = 1;
		nb = -nb;
	}
	while (nb > 0)
	{
		nb = nb / 10;
		++i;
	}
	return (i);
}
